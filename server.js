// x402 protected endpoint — returns all registered agents
app.get('/api/agents', x402Middleware, (req, res) => {
  const url = `${GITHUB_API_BASE}/contents/memory/agents`;

  https.get(url, {
    headers: { 'User-Agent': 'nullpriest-server' }
  }, (upstream) => {
    let data = '';
    upstream.on('data', chunk => data += chunk);
    upstream.on('end', () => {
      try {
        const files = JSON.parse(data);
        const agentFiles = files.filter(f => f.name.endsWith('.json'));

        // Fetch each agent JSON file
        let fetched = 0;
        const agents = [];

        if (agentFiles.length === 0) {
          return res.json({ agents: [], count: 0 });
        }

        agentFiles.forEach(file => {
          https.get(file.download_url, (agentStream) => {
            let agentData = '';
            agentStream.on('data', chunk => agentData += chunk);
            agentStream.on('end', () => {
              try {
                agents.push(JSON.parse(agentData));
              } catch (e) {
                console.error(`Failed to parse ${file.name}:`, e.message);
              }
              fetched++;

              if (fetched === agentFiles.length) {
                res.json({ agents, count: agents.length });
              }
            });
          }).on('error', (err) => {
            console.error(`Failed to fetch ${file.name}:`, err.message);
            fetched++;
            if (fetched === agentFiles.length) {
              res.json({ agents, count: agents.length });
            }
          });
        });
      } catch (e) {
        res.status(500).json({ error: 'Failed to parse GitHub response', message: e.message });
      }
    });
  }).on('error', (err) => {
    res.status(500).json({ error: 'Failed to fetch from GitHub', message: err.message });
  });
});


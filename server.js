// Proxies memory/*.md files from GitHub repo raw content
// Returns decoded, human-readable content directly
app.get('/api/memory/:filename', (req, res) => {
  const filename = req.params.filename;
  const url = `${GITHUB_RAW_BASE}/memory/${filename}`;

  https.get(url, (upstream) => {
    if (upstream.statusCode !== 200) {
      return res.status(upstream.statusCode).json({
        error: `GitHub returned ${upstream.statusCode}`,
        file: filename
      });
    }

    let data = '';
    upstream.on('data', chunk => data += chunk);
    upstream.on('end', () => {
      res.setHeader('Content-Type', 'text/plain; charset=utf-8');
      res.setHeader('Cache-Control', 'public, max-age=60');
      res.send(data);
    });
  }).on('error', (err) => {
    res.status(500).json({ error: 'Failed to fetch from GitHub', message: err.message });
  });
});


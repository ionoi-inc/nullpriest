app.get('/api/agents', x402Middleware, (req, res) => {
  const url = `${GITHUB_API_BASE}/contents/memory/agents.json`;
  https.get(url, { headers: { 'User-Agent': 'nullpriest' } }, (apiRes) => {
    let data = '';
    apiRes.on('data', chunk => data += chunk);
    apiRes.on('end', () => {
      if (apiRes.statusCode !== 200) {
        return res.status(502).json({ error: 'GitHub API error', status: apiRes.statusCode });
      }
      try {
        const file = JSON.parse(data);
        const agents = JSON.parse(Buffer.from(file.content, 'base64').toString('utf8'));
        res.json(agents);
      } catch (err) {
        res.status(500).json({ error: 'Parse error', details: err.message });
      }
    });
  }).on('error', err => {
    res.status(500).json({ error: 'Fetch failed', details: err.message });
  });
});

